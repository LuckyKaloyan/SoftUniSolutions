package SoftUniJavaOOP.OOP.WorkingWithAbstraction.Lab.PointInRectangle;

public class PointsInRectangle {

    int x;
    int y;

    public PointsInRectangle(int x, int y){
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }
    public int getY() {
        return y;
    }
}